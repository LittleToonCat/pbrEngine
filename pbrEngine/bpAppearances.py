def validate():
    for avatar in data["avatars"]:
        print("{} (Id {})".format(avatar["name"], avatar["id"]))
        for cat, options in avatar["appearance_options"].items():
            print("\t{:10}".format(cat), end="")
            option_ids = [option["id"] for option in options]
            print("Ids: 0-{}".format(len(option_ids) - 1))
            if len(option_ids) != len(set(option_ids)):  # check for duplicate ids
                raise ValueError(options, option_ids)
            elif min(option_ids) != 0:  # check for not starting at zero
                raise ValueError(options, option_ids)
            elif max(option_ids) != len(option_ids) - 1:  # check for gaps
                raise ValueError(options, option_ids)
            elif option_ids != sorted(option_ids):  # check for out of order
                raise ValueError(options, option_ids)
            if any([not option["description"] for option in options]):
                raise ValueError(options)
            if any([not option["description"][-1:] in ".!?\"" for option in options]):
                raise ValueError(options)
            if any([not option["name"] for option in options]):
                raise ValueError(options)
        if not avatar["description"] or not avatar["name"]:
            raise ValueError()


